          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Cómo se propaga la COVID 19?</b> <br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              Una persona puede contraer la COVID 19 por contacto con otra que esté infectada por el virus. La
              enfermedad se propaga principalmente de persona a persona a través de las gotículas que salen despedidas
              de la nariz o la boca de una persona infectada al toser, estornudar o hablar. Estas gotículas son
              relativamente pesadas, no llegan muy lejos y caen rápidamente al suelo. Una persona puede contraer la
              COVID 19 si inhala las gotículas procedentes de una persona infectada por el virus. Por eso es importante
              mantenerse al menos a un metro de distancia de los demás. Estas gotículas pueden caer sobre los objetos y
              superficies que rodean a la persona, como mesas, pomos y barandillas, de modo que otras personas pueden
              infectarse si tocan esos objetos o superficies y luego se tocan los ojos, la nariz o la boca. Por ello es
              importante lavarse las manos frecuentemente con agua y jabón o con un desinfectante a base de alcohol. La
              OMS está estudiando las investigaciones en curso sobre las formas de propagación de la COVID 19 y seguirá
              informando sobre las conclusiones que se vayan obteniendo.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Es posible contagiarse de COVID 19 por contacto con una persona que no presente
                ningún síntoma?</b> <br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              La principal forma de propagación de la COVID 19 es a través de las gotículas respiratorias expelidas por
              alguien que tose o que tiene otros síntomas como fiebre o cansancio. Muchas personas con COVID 19
              presentan solo síntomas leves. Esto es particularmente cierto en las primeras etapas de la enfermedad. Es
              posible contagiarse de alguien que solamente tenga una tos leve y no se sienta enfermo. Según algunas
              informaciones, las personas sin síntomas pueden transmitir el virus. Aún no se sabe con qué frecuencia
              ocurre. La OMS está estudiando las investigaciones en curso sobre esta cuestión y seguirá informando sobre
              las conclusiones que se vayan obteniendo.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Cómo podemos protegernos a nosotros mismos y a los demás si no sabemos quién
                está infectado?</b><br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              Practicar la higiene respiratoria y de las manos es importante en TODO momento y la mejor forma de
              protegerse a sí mismo y a los demás. Cuando sea posible, mantenga al menos un metro de distancia entre
              usted y los demás. Esto es especialmente importante si está al lado de alguien que esté tosiendo o
              estornudando. Dado que es posible que algunas personas infectadas aún no presenten síntomas o que sus
              síntomas sean leves, conviene que mantenga una distancia física con todas las personas si se encuentra en
              una zona donde circule el virus de la COVID 19.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Qué relación hay entre la COVID-19 y los animales?</b><br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              La COVID 19 se propaga por transmisión entre seres humanos. Conocemos bastantes datos sobre otros virus de
              la familia de los coronavirus, y la mayoría de estos tipos de virus tienen su origen en animales. El virus
              de la COVID 19 (también llamado SARS-CoV-2) es un nuevo virus en los humanos. La posible fuente animal de
              la COVID 19 aún no ha sido confirmada, pero se está investigando. La OMS sigue monitoreando las últimas
              investigaciones sobre este y otros temas relacionados con la COVID 19 y proporcionará información
              actualizada a medida que se disponga de nuevos datos.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Cuánto tiempo sobrevive el virus en las superficies?</b><br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              Lo más importante que hay que saber sobre el contacto del coronavirus con superficies es que estas se
              pueden limpiar fácilmente con desinfectantes domésticos comunes que matarán el virus. Diversos estudios
              han demostrado que el virus de la COVID 19 puede sobrevivir hasta 72 horas en superficies de plástico y
              acero inoxidable, menos de 4 horas en superficies de cobre y menos de 24 horas en superficies de cartón.
              Como siempre, lávese las manos con un desinfectante a base de alcohol o con agua y jabón. Evite tocarse
              los ojos, la boca o la nariz.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

          <div class="testimonial-item">
            <p>
              <b style="color:#2c4964;">¿Cómo puedo comprar con seguridad en las tiendas de comestibles?</b><br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              En las tiendas de comestibles, mantenga al menos un metro de distancia con los demás y no se toque los
              ojos, la nariz o la boca. Si es posible, desinfecte los mangos y barras de los carritos o cestas antes de
              comprar. Cuando regrese a casa, lávese las manos a fondo al llegar y también después de coger y almacenar
              los productos que ha comprado. Actualmente no hay ningún caso confirmado de contagio de la COVID 19 a
              través de los alimentos o de sus envases.
              <i class="bx bxs-quote-alt-right quote-icon-right"></i>
            </p>
          </div>

