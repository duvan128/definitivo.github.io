            <p>
              <b style="color:#2c4964;">¿Qué debo hacer si tengo síntomas de COVID-19 y cuándo he de buscar atención
                médica?</b> <br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              Si tiene síntomas leves, como tos o fiebre leves, generalmente no es necesario que busque atención médica.
              Quédese en casa, aíslese y vigile sus síntomas. Siga las orientaciones nacionales sobre el
              autoaislamiento. Sin embargo, si vive en una zona con paludismo (malaria) o dengue, es importante que no
              ignore la fiebre. Busque ayuda médica. Cuando acuda al centro de salud lleve mascarilla si es posible,
              manténgase al menos a un metro de distancia de las demás personas y no toque las superficies con las
              manos. En caso de que el enfermo sea un niño, ayúdelo a seguir este consejo. Busque inmediatamente
              atención médica si tiene dificultad para respirar o siente dolor o presión en el pecho. Si es posible,
              llame a su dispensador de atención de la salud con antelación para que pueda dirigirlo hacia el centro de
              salud adecuado.
