                <div id="morecsspure-toggled-element">
                  <p>En un principio, el brote parecía restringirse a los países limítrofes, pero a comienzos de enero
                    empezaron a conocerse los primeros casos más allá del continente asiático. Australia y Estados
                    Unidos fueron los primeros afectados dentro de esta expansión, mientras que los tres casos
                    detectados en Francia el 25 de enero confirmaban la entrada del nuevo coronavirus en Europa. Desde
                    entonces, el brote ya ha llegado a 47 países del Viejo Continente, con Rusia y España como los
                    países más afectados.
                    <br>
                    <br>
                    En cuanto a la mortalidad de este nuevo coronavirus, ya son más de 530.000 personas las que han
                    perdido la vida como consecuencia de este brote. El número de víctimas mortales supera así al
                    registrado por el síndrome respiratorio agudo grave (SARS), que se propagó a nivel internacional
                    entre noviembre de 2002 y julio de 2003. El virus, descubierto en la también provincia china de
                    Cantón, causó la muerte de 774 personas. Pese a ello, según estimaciones de la OMS, la capacidad de
                    contagio de aquel virus sigue siendo superior. Y es que, mientras que el agente detrás de la
                    COVID-19 contagia de promedio a entre 1,4 y 2,5 personas por enfermo, el SARS-CoV podía llegar a
                    transmitirse a cinco personas por cada afectado. Asimismo, el nuevo coronavirus se encuentra aún
                    lejos del virus de Marburgo (MARV). El MARV, que provoca una enfermedad hemorrágica severa, fue
                    detectado por primera vez en 1967 y es, por ahora, el virus más mortífero de los últimos 50 años con
                    una tasa de mortalidad del 80%.
                    <br>
                    <br>
                    Pero las consecuencias de la “neumonía de Wuhan” no son solo sociales y humanas. Especialmente en el
                    caso del continente en el que se encuentra el epicentro del brote. El valor de los índices
                    bursátiles de las principales bolsas asiáticas ha caído de forma drástica desde principios de 2020,
                    siendo las de Shanghái y Hong Kong las más perjudicadas. Eso sí, ya no son las únicas. En España, el
                    IBEX-35 experimentó una caída muy marcada durante los primeros días de marzo y a fecha de 16 de
                    marzo se situaba ya por debajo de los 7.000 puntos.</p>
