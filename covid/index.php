            <p>
              <b style="color:#2c4964;">¿Son eficaces los antibióticos para prevenir o tratar la COVID 19?</b><br>
              <i class="bx bxs-quote-alt-left quote-icon-left"></i>
              No. Los antibióticos no son eficaces contra los virus, solo contra las infecciones bacterianas. La COVID
              19 está causada por un virus, de modo que los antibióticos no sirven frente a ella. No se deben usar
              antibióticos como medio de prevención o tratamiento de la COVID 19. En los hospitales, los médicos a veces
              utilizan antibióticos para prevenir o tratar infecciones bacterianas secundarias que pueden ser una
              complicación de la COVID 19 en pacientes gravemente enfermos. Solo deben usarse para tratar una infección
              bacteriana siguiendo las indicaciones de un médico.
