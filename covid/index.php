                  <p>El SARS-CoV-2 ya afecta a más de 11 millones de personas en todo el mundo. La también conocida como
                    “neumonía de Wuhan” fue anunciada de forma oficial por la Organización Mundial de la Salud (OMS) el
                    31 de diciembre de 2019. Lo hacía así unas tres semanas después de la aparición de una veintena de
                    casos de un Síndrome Respiratorio Agudo Severo de origen desconocido en uno de los mercados de la
